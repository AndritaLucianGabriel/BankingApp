package Service;

public class CardValidation {
    public boolean validateCardNumber(String cardNumber){return cardNumber.matches("^(?:4[0-9]{12}(?:[0-9]{3})?|[25][1-7][0-9]{14}|6(?:011|5[0-9][0-9])[0-9]{12}|3[47][0-9]{13}|3(?:0[0-5]|[68][0-9])[0-9]{11}|(?:2131|1800|35\\d{3})\\d{11})$"); }
    public boolean validatePin(int PIN){return PIN>0;}
    public boolean validateIssueDate(String issueDate){return issueDate.matches("^(19|20)\\d\\d[- /.](0[1-9]|1[012])[- /.](0[1-9]|[12][0-9]|3[01])$");}
}
